//FUNCIONES

function promedio(a,b,c,d,e,f){  //palabra definida 'function' permite crear funciones
    return (a+b+c+d+e+f)/6;
}

function welcome(nom,ap,curso){
    return "Bienvenido "+nom+" "+ap+". Estas por entrar al curso de "+curso;
}

//llamado de funciones
console.log(promedio(10,6,7,9,10,8));
console.log(welcome("Sergio","Palapa","Javascript"));



